<h3>Projekt</h3>
<p>
	W ramach projektu stworzyłem bazę zarządzania osiedlem studenckim. Ma on na celu ułatwienie administracji obsługę studentów oraz pracowników. Każdy pracownik ma przypisane stanowisko oraz można zapisać w których akademikach pracuje. Studenci są przypisani do swoich pokoi w odpowiednich akademikach, mają też możliwość zarejestrowania komputerów w sieci akademickiej. W celu ewentualnej identyfikacji właściciela hosta, wszelkie zmiany danych dotyczących komputerów są archiwizowane. Archiwizacja opiera się o 2 triggery, oddzielny dla operacji UPDATE i DELETE.
	Interfejs umożliwia przegląd, edycje oraz usunięcie studentów z listy. Można przeglądać, edytować i usuwać zarejestrowane urządzenia studentów oraz przeglądać historię ich edycji. Lista pracowników ma podobne opcje, tzn. przegląd, edycja oraz usunięcie. 

</p>











